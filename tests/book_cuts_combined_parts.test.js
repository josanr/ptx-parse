'use strict';
const CutRiteLink = require('../src/cutritelink');
const fs = require('fs');
const iconv = require('iconv-lite');

test("parse PTX with multiple sheets in book and different parts on sheet", () => {
    const parser = new CutRiteLink();

    const file = fs.readFileSync('./tests/253794-10.ptx');
    const convertedFile = iconv.decode(file, 'win1251');

    const maps = parser.parse(convertedFile.toString());
    const resPartCount = {};

    for(let gid in maps.goods){

        for(let mid in maps.goods[gid]){

            for(let cid in maps.goods[gid][mid].cuts){
                const cut = maps.goods[gid][mid].cuts[cid];
                if(cut.id === 'Обрезок'){
                    continue;
                }
                if(resPartCount[cut.id] === undefined){
                    resPartCount[cut.id] = 0;
                }
                resPartCount[cut.id]++;
            }
        }
    }

    expect(Object.keys(resPartCount).length).toBe(Object.keys(partCount).length);
    for(let pid in resPartCount){
        expect(resPartCount[pid]).toBe(partCount[pid].quant);
    }

});

